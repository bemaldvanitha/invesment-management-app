class ForexRate {
    constructor(id,from,to,open,high,low,close,date) {
        this.id = id;
        this.from = from;
        this.to = to;
        this.open = open;
        this.high = high;
        this.low = low;
        this.close = close;
        this.date = date;
    }
}

export default ForexRate;